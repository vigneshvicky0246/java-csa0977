public class Q3_BitwiseAndExample {
    public static void main(String[] args) {
        int num1 = 10;  // binary representation: 1010
        int num2 = 6;   // binary representation: 0110

        int result = num1 & num2;

        System.out.println("num1 & num2 = " + result); // prints 2 (binary representation: 0010)
    }
}